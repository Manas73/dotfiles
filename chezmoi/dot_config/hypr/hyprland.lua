--------------------------------------------------------------------------------
---                      HYPRLAND MODULAR CONFIGURATION                      ---
--------------------------------------------------------------------------------
--
-- This configuration follows SOLID principles for better maintainability:
--
-- - Single Responsibility: Each module handles one aspect of configuration
-- - Open/Closed: Easy to extend without modifying existing files
-- - Liskov Substitution: Similar configs follow same patterns
-- - Interface Segregation: Load only what you need
-- - Dependency Inversion: Main config depends on abstractions (modules)
--
-- Structure:
--   conf.d/     - Core configuration (monitors, input, appearance, etc.)
--   keybinds/   - Keybinding modules (base, applications, workspaces, etc.)
--   rules/      - Window and layer rules
--   colors/     - Theme colors
--
--------------------------------------------------------------------------------

-- Make ~/.config/hypr/ requireable so `require("conf.d.monitors")` resolves to
-- ~/.config/hypr/conf.d/monitors.lua, etc.
local hypr_root = os.getenv("HOME") .. "/.config/hypr"
package.path = hypr_root .. "/?.lua;"
            .. hypr_root .. "/?/init.lua;"
            .. package.path

-- Colors & theming (generated by matugen). Exposed as a global so every
-- sub-module can read colors.primary, colors.surface, etc.
_G.colors = require("colors.matugen")

-- Core configuration
require("conf.monitors")
require("conf.environment")
require("conf.programs")
require("conf.autostart")
require("conf.input")
require("conf.appearance")
require("conf.layouts")

-- Keybindings
require("keybinds.base")
require("keybinds.applications")
require("keybinds.window-mgmt")
require("keybinds.workspaces")
require("keybinds.submaps")

-- Rules
require("rules.layer-rules")
require("rules.window-rules")
